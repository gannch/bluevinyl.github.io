function adjustsize() {
  $height = $(".section1").width() * 0.75;
  $height = $height.toString();
  $(".section1").css("height", $height + "px");
  }

$(document).ready(function() {
  adjustsize();
  $(window).resize(function() {
    adjustsize();
    });
  });

